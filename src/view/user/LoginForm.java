    private void login_btnActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_login_btnActionPerformed
        // TODO add your handling code here:
        String username = usernameInput.getText();
        String password = passwordInput.getText();
        User user = new UserController().login(username, password);
        if (user != null) {
            if (user.getRole().equals("gamer")) {
                new UserMenu().setVisible(true);
                this.dispose();
            } else if (user.getRole().equals("admin")) {
                new AdminMenu().setVisible(true);
                this.dispose();
            } else if (user.getRole().equals("developer")) {
                new DeveloperMenu().setVisible(true);
                this.dispose();
            }
            new File_Storage().saveLoggedUser(user);
        } else {
            ErrorDialog dialog = new ErrorDialog(this, true);
            dialog.setErrorMsg("Login failed");
            dialog.setVisible(true);
        }
    }// GEN-LAST:event_login_btnActionPerformed

